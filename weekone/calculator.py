def add(n):
    return n + n

def subtract(n):
    return n - n

def divide(n):
    return n / n

def multiply(n):
    return n * n

def floor(n):
    return n // n

def power(n):
    return n**n


print(add(100))
print(subtract(50))
print(divide(89))
print(multiply(78))
print(floor(34))
print(power(45))